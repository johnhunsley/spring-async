package com.hunsley.async;

/**
 * <p>
 *     An enumeration to depict Bank Account type
 * </p>
 * @author johnhunsley
 */
public enum AccountType {
    /**
     * An ISA type bank account
     */
    ISA,

    /**
     * A Current account
     */
    CURRENT,

    /**
     * A Savings account
     */
    SAVINGS,

    /**
     * A joint account owned by two account holders
     */
    JOINT
}
